#include <unistd.h>

void ft_putnbr(int nb)
{
    long n;
    char a;
    n=nb;
    if(n<0)
    {
        write(1,"-",1);
        n=-n;
    }
    if(n>=10)
        ft_putnbr(n/10);
    a='0'+n%10;
    write(1,&a,1);
}

